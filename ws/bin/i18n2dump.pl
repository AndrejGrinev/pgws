# i18n2dump.pl - Convert i18n template to SQL dump
# Usage: perl $BIN/i18n2dump.pl $lang < $BLD/$schema.i18n > $dest/20_dump.sql

use strict;

use utf8;
use Locale::Maketext::Simple('Path' => 'pkg/i18n/src/database');

#----------------------------------------------------------------------

my $lang = shift;

my $schema_fmt = 'i18n_%s';

my $name = sprintf $schema_fmt, 'def';
my $lname = sprintf $schema_fmt, $lang;

#----------------------------------------------------------------------
print <<EOF;
/*

    Copyright (c) 2010, 2012 Tender.Pro http://tender.pro.
    [SQL_LICENSE]

    ATTENTION! This file was AUTOGENERATED by pgws/ws/bin/i18n2dump.pl $lang

*/

EOF

loc_lang($lang);
while (<>) {
  if (/CREATE SCHEMA/ || /^SET client_min_messages = warning;/) {
    next;
  }
  s/$name/$lname/g;
  s/{{(.+)}}/_l($1)/ge;
  print $_;
}

#----------------------------------------------------------------------
sub _l {
  my ($s, @a) = @_;
  utf8::upgrade($s);
  $s=loc($s, @a);
  $s=~ s/'/''/g; #'
  return "'$s'";
}

1;
